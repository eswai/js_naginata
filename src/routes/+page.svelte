<h4>物語を書くための配列薙刀式</h4>

<p>
  カナ配列薙刀式は、物語文、長文を書くときに速く疲れないことを目的とした配列です。作者は大岡俊彦氏です。
</p>
<p>
  薙刀式マニュアル「第1章 薙刀式を打てるようになろう」のレッスン1からレッスン8にそって、薙刀式の練習をするためのサイトです。
  DvorakJなどの特別なソフトウェアをインストールしなくても薙刀式の練習ができるように、
  テキスト入力欄ではJavaScriptでカナ変換を行なっています。
  気軽に薙刀式を体験することができます。
  英字入力モードのまま入力してください。(SandS機能が有効だとスペースキーの同時押しが効きません)
  カナ入力と一部の編集モードに対応しています。
  入力したカナの漢字変換はできません。
</p>

